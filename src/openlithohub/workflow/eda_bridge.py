"""Bridge scripts for commercial EDA tools (Calibre, IC Validator).

OpenLithoHub does not implement full DRC — these helpers emit minimal,
human-editable rule decks that load an exported ``.oas`` and run the most
basic checks (min width, min spacing). The intent is to remove the friction
of "I have OASIS, now what?" for layout engineers, not to replace a real
sign-off deck.

The emitted files are pure text. No EDA tool is invoked here.

Unit handling (issue #50)
-------------------------

The Calibre template uses ``PRECISION 1000`` (1000 dbu/µm = 1 dbu/nm)
and threshold literals are emitted in **microns** (``min_width_nm /
1000``). SVRF interprets bare numerics on ``INTERNAL ... < N`` /
``EXTERNAL ... < N`` directives as user units (microns by default), so
emitting ``< 40`` for a 40-nm rule would have meant "< 40 µm" — i.e.
the rule check would be 1000× too lax and would never flag a real
violation. The IC Validator template has the same convention.

Run any deck-emission test that pins literal text against this
nm-vs-µm shift if you change the format strings.

Geometric scope (issue #51)
---------------------------

The Calibre ``INTERNAL mask < N ABUT < 90 SINGULAR REGION`` and the ICV
``internal1(mask, < N)`` directives are measured **edge-to-edge**, which
is well-defined for Manhattan polygons but can produce direction-dependent
results on curvilinear / concave geometry (the classic case: a notch on
the inside of an arc, where the closest "internal" pair of edges is the
arc-tangent rather than the notch wall). For Manhattan OPC output (the
default of ``workflow.contour.manhattan``) this template is correct. For
curvilinear ILT output (``workflow.contour.curvilinear``), replace
``INTERNAL ... ABUT < 90`` with ``INTERNAL ... PROJECTING < 90`` (Calibre)
or pair ``internal1`` with a ``with_radius`` curvature filter (ICV) so
arc-segment curvature is not mis-classified as a width violation. We
ship the Manhattan-correct deck because it's the headline OPC path; the
curvilinear deck is a roadmap item.
"""

from __future__ import annotations

import re
from dataclasses import dataclass
from pathlib import Path

# Cell names follow OASIS / Verilog identifier rules — letters, digits,
# underscore, dollar; must start with a letter or underscore. Restricting
# to this set keeps a malicious cell_name from injecting SVRF directives
# (e.g. SYSTEM, INCLUDE) into the rule deck via embedded quotes/newlines.
_CELL_NAME_RE = re.compile(r"^[A-Za-z_][A-Za-z0-9_$]*$")


def _validate_cell_name(cell_name: str) -> None:
    if not _CELL_NAME_RE.match(cell_name):
        raise ValueError(
            f"Invalid cell_name {cell_name!r}: must match {_CELL_NAME_RE.pattern} "
            "(OASIS/Verilog identifier rules) — refusing to interpolate "
            "untrusted text into a Calibre/ICV rule deck."
        )


def _validate_oasis_path(oasis_path: Path) -> None:
    s = str(oasis_path)
    if '"' in s or "\n" in s or "\r" in s:
        raise ValueError(
            f"Invalid oasis_path {s!r}: must not contain quotes or newlines — "
            "refusing to interpolate untrusted text into a Calibre/ICV rule deck."
        )


@dataclass(frozen=True)
class BridgeRules:
    """Minimal DRC rules used by the emitted templates."""

    min_width_nm: float
    min_spacing_nm: float
    layer: int = 1
    datatype: int = 0


_CALIBRE_TEMPLATE = """\
// Auto-generated by OpenLithoHub — minimal Calibre nmDRC rule deck.
// Edit freely; this is a starting point, not a sign-off deck.

LAYOUT PATH "{oasis_path}"
LAYOUT PRIMARY "{cell_name}"
LAYOUT SYSTEM OASIS

DRC RESULTS DATABASE "{cell_name}.results.db" ASCII
DRC SUMMARY REPORT "{cell_name}.summary.rep"

PRECISION 1000
RESOLUTION 1

LAYER MASK {layer} DATATYPE {datatype}

mask = COPY MASK

min_width {{
   @ Minimum width on MASK is {min_width_nm} nm.
   INTERNAL mask < {min_width_um} ABUT < 90 SINGULAR REGION
}}

min_spacing {{
   @ Minimum spacing on MASK is {min_spacing_nm} nm.
   EXTERNAL mask < {min_spacing_um} ABUT < 90 SINGULAR REGION
}}
"""

_ICV_TEMPLATE = """\
// Auto-generated by OpenLithoHub — minimal Synopsys IC Validator rule file.
// Run with: icv -i {oasis_path} -f OASIS -top {cell_name} <this_file>.rs

layout("{oasis_path}", OASIS, "{cell_name}");

mask = assign({{ {{ {layer}, {datatype} }} }});

min_width = internal1(mask, < {min_width_um});
output(min_width, {{ {{ 1000, 0 }} }}, "min_width_violations");

min_spacing = external1(mask, < {min_spacing_um});
output(min_spacing, {{ {{ 1001, 0 }} }}, "min_spacing_violations");
"""

_RUN_README = """\
# Running the {tool} bridge deck

Generated by OpenLithoHub for `{oasis_path}` (cell `{cell_name}`).

## Calibre

```sh
calibre -drc -hier {svrf_path}
```

## IC Validator

```sh
icv -dp 4 -i {oasis_path} -f OASIS -top {cell_name} {runset_path}
```

These decks only check minimum width / minimum spacing. Replace with your
foundry's qualified deck before sign-off.
"""


def emit_calibre_svrf(
    oasis_path: str | Path,
    rules: BridgeRules,
    *,
    cell_name: str = "TOP",
    output_path: str | Path | None = None,
) -> Path:
    """Emit a minimal Calibre nmDRC ``.svrf`` rule deck next to the OASIS file."""
    _validate_cell_name(cell_name)
    oasis_path = Path(oasis_path)
    _validate_oasis_path(oasis_path)
    out = Path(output_path) if output_path else oasis_path.with_suffix(".svrf")
    out.parent.mkdir(parents=True, exist_ok=True)
    out.write_text(
        _CALIBRE_TEMPLATE.format(
            oasis_path=str(oasis_path),
            cell_name=cell_name,
            layer=rules.layer,
            datatype=rules.datatype,
            min_width_nm=rules.min_width_nm,
            min_spacing_nm=rules.min_spacing_nm,
            min_width_um=rules.min_width_nm / 1000.0,
            min_spacing_um=rules.min_spacing_nm / 1000.0,
        ),
        encoding="utf-8",
    )
    return out


def emit_icv_runset(
    oasis_path: str | Path,
    rules: BridgeRules,
    *,
    cell_name: str = "TOP",
    output_path: str | Path | None = None,
) -> Path:
    """Emit a minimal Synopsys IC Validator runset next to the OASIS file."""
    _validate_cell_name(cell_name)
    oasis_path = Path(oasis_path)
    _validate_oasis_path(oasis_path)
    out = Path(output_path) if output_path else oasis_path.with_suffix(".rs")
    out.parent.mkdir(parents=True, exist_ok=True)
    out.write_text(
        _ICV_TEMPLATE.format(
            oasis_path=str(oasis_path),
            cell_name=cell_name,
            layer=rules.layer,
            datatype=rules.datatype,
            min_width_um=rules.min_width_nm / 1000.0,
            min_spacing_um=rules.min_spacing_nm / 1000.0,
        ),
        encoding="utf-8",
    )
    return out


def emit_bridge_bundle(
    oasis_path: str | Path,
    rules: BridgeRules,
    *,
    cell_name: str = "TOP",
) -> dict[str, Path]:
    """Emit Calibre + IC Validator templates and a short README for the bundle."""
    _validate_cell_name(cell_name)
    oasis_path = Path(oasis_path)
    _validate_oasis_path(oasis_path)
    svrf = emit_calibre_svrf(oasis_path, rules, cell_name=cell_name)
    runset = emit_icv_runset(oasis_path, rules, cell_name=cell_name)
    readme = oasis_path.with_suffix(".bridge.md")
    readme.write_text(
        _RUN_README.format(
            tool="Calibre / IC Validator",
            oasis_path=str(oasis_path),
            cell_name=cell_name,
            svrf_path=str(svrf),
            runset_path=str(runset),
        ),
        encoding="utf-8",
    )
    return {"svrf": svrf, "icv": runset, "readme": readme}
